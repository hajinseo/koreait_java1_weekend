package com.kita.first.level1;

public class Operator1 {
	public static void main(String[] args) {
		int n1 = 2;
		int n2 = 5;
		int result1 = n1 + n2;
		System.out.println(result1 + "\n");
		n1++;
		n1 = n1 + 1;
		n1 += 1;
		System.out.println(n1 + "\n");
		System.out.println(n2 + "\n");
		}	
}
