package com.kita.first.level1;

public class Operator4 {
	public static void main(String[] args) {
		//대입 연산자 기본
		int n = 7;
		n += 4;
		System.out.println(n); //11
		n -= 4;
		System.out.println(n); //7
		n *= 4;
		System.out.println(n); //28
		n /= 4;
		System.out.println(n); //7
		n %= 4;
		System.out.println(n); //3
	}
}
